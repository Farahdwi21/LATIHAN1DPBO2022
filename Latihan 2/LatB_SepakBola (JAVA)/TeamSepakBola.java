class TeamSepakBola{

	//atribut
	private String nama_team;
	private String negara_asal;
	private String tahun_berdiri;
	private String nama_pemain;
	//atribut  tambahan
	private String nama_kiper;
	
	//constructor	
	TeamSepakBola(){
	}
		
	//get & set untuk atribut Nama Team	
	public void setNamaTeam(String nama_team){
		this.nama_team = nama_team;
	}
		
	public String getNamaTeam(){
		return this.nama_team;
	}
	
	//get & set untuk atribut Negara Asal
	public void setNegaraAsal(String negara_asal){
		this.negara_asal = negara_asal;
	}
		
	public String getNegaraAsal(){
		return this.negara_asal;
	}
	
	//get & set untuk atribut Tahun Berdiri
	public void setTahun(String tahun_berdiri){
		this.tahun_berdiri = tahun_berdiri;
	}
		
	public String getTahun(){
		return this.tahun_berdiri;
	}
	
	//get & set untuk atribut Nama Pemain	
	public void setNamaPemain(String nama_pemain){
		this.nama_pemain = nama_pemain;
	}
		
	public String getNamaPemain(){
		return this.nama_pemain;
	}

	//get & set untuk aribut Nama Kiper	
	public void setNamaKiper(String nama_kiper){
		this.nama_kiper = nama_kiper;
	}
		
	public String getNamaKiper(){
		return this.nama_kiper;
	}
}


		
		